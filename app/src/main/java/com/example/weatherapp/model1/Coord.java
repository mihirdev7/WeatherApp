package com.example.weatherapp.model1;

public class Coord {
    private double lon;
    private double lat;


    // Getter Methods 

    public double getLon() {
        return lon;
    }

    public double getLat() {
        return lat;
    }

    // Setter Methods 

    public void setLon(double lon) {
        this.lon = lon;
    }

    public void setLat(double lat) {
        this.lat = lat;
    }
}
